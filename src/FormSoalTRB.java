    private void simpanData(){
        String file = "./Hasil.txt";
        String nama = labelNama.getText();
        String jur = labelJurusan.getText();
        int total = total_benar;
        String waktu = labelTglUjian.getText();
        BufferedWriter writer = null;
        try {
            writer = new BufferedWriter(new FileWriter(file, true));
            writer.write(nama+"; "+jur+"; "+String.valueOf(total)+"; "+waktu);
            writer.flush();
            writer.newLine();
            writer.close();
            //JOptionPane.showMessageDialog(rootPane, "OK");
        } catch (IOException ex) {
            Logger.getLogger(FormSoalOto.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(rootPane, "Gagal Simpan");
        } finally {
            try{
                if (writer != null)
                    writer.close();
            } catch (IOException ex) {
                Logger.getLogger(FormSoalOto.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }

