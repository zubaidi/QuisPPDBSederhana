    private void initMulai() {
        timer = new Timer(interval, new ActionListener() {

            public void actionPerformed(ActionEvent e) {
                if (seconds == 0 && (minutes > 0 || hours > 0)) {
                    seconds = 59;
                    minutes--;
                } else {
                    seconds--;
                }
                if (minutes == 0 && hours > 0) {
                    minutes = 59;
                    hours--;
                }
                if (seconds == 0 && minutes == 0 && hours == 0) {
                    seconds = minutes = hours = 0;
                    labelTimer.setText("SELESAI");
                    setHasil();
                }
                TimeScan ts = new TimeScan(seconds, minutes, hours);
                if (minutes >= 0 && hours > 0 && seconds >= 0) {
                    labelTimer.setText(ts.getHours() + ":" + ts.getMinutes() + ":" + ts.getSeconds());
                } else if (minutes > 0 && seconds >= 0 && hours <= 0) {
                    labelTimer.setText(ts.getMinutes() + ":" + ts.getSeconds());
                } else if (minutes == 0 && hours == 0 && seconds > 0) {
                    if (seconds > 10) {
                        labelTimer.setText(ts.getSeconds());
                    } else {
                        labelTimer.setText(String.valueOf(seconds));
                        labelTimer.setForeground(Color.red);
                    }
                }
            }
        });
    }
    
